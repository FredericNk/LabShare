<template>
  <div class="profile">
    <h1>{{ $t("changePassword.title") }}</h1>

    <template v-if="updated">
      <div class="alert alert-success">{{ $t('changePassword.successMessage') }}</div>
    </template>

    <template v-if="loading">
      <div class="alert alert-warning">{{ $t("general.loading") }}</div>
    </template>

    <template v-if="!updated && !loading">
      <div v-if="error" class="alert alert-danger">{{ error }}</div>

      <div class="row">
        <b-form class="col-md-6">
          <InputForm inType="password" name="changePassword.currentPassword" v-model="formData.oldPassword"></InputForm>
          <Password v-model="formData.newPassword" verticalLabel @validPassword="valid"></Password>

          <b-button variant="primary" @click="submit" :disabled="disableSubmit">{{ $t('general.save') }}</b-button>
        </b-form>
      </div>
    </template>
  </div>
</template>

<script>
import InputForm from "@/components/InputForm";
import Password from "@/components/PasswordFields";

export default {
  name: "ChangePassword",
  data: function() {
    return {
      updated: false,
      loading: false,
      error: null,
      disableSubmit: true,
      formData: {
        oldPassword: "",
        newPassword: ""
      }
    };
  },

  methods: {
    submit: function() {
      this.error = null;
      this.loading = true;
      this.$http.post("change-password", this.formData).then(() => {
          this.updated = true;
          this.loading = false;
          setTimeout(() => (this.updated = false), 3000);
        },
        error => {
          this.error = error.body.errorDescription;
          this.loading = false;
        }
      );
    },
    valid(valid) {
      this.disableSubmit = !valid;
    }
  },
  components: {
    InputForm,
    Password
  }
};
</script>
